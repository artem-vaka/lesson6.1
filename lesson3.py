grade = int(input('Введіть вашу оцінку (від 1 до 5):'))

if grade ==1:
    print('Дуже погано')
elif grade ==2:
    print('Погано')
elif grade ==3:
    print('Задовільно')
elif grade ==4:
    print('Добре')
elif grade ==5:
    print('Відмінно')
else:
    print('Помилка: оцінка має бути від 1 до 5.')
